# chu vi hinh vuong
def C_vg(a):
    return 4*a

# dien tich hinh vuong.    
def S_vg(a):
    return a*a

# chu vi hinh chu nhat.
def C_hcn(dai,rong):
    return 2*(dai+rong)

# dien tich hinh chu nhat.   
def S_hcn(dai,rong):
    return dai*rong

# chu vi hinh tron.    
def C_tron(r):
    return 2*3.14*r

# dien tich hinh tron.   
def S_tron(r):
    return r*r*3.14
