# Ham tinh chu vi hinh vuong.
def CVHV(canh):
    return 4*canh

# Ham tinh dien tich hinh vuong.    
def DTHV(canh):
    return canh*canh

# Ham tinh chu vi hinh chu nhat.
def CVHCN(dai,rong):
    return 2*(dai+rong)

# Ham tinh dien tich hinh chu nhat.   
def DTHCN(dai,rong):
    return dai*rong

# Ham tinh chu vi hinh tron.    
def CVHT(bk):
    return 2*3.14*bk

# Ham tinh dien tich hinh tron.   
def DTHT(bk):
    return bk*bk*3.14
